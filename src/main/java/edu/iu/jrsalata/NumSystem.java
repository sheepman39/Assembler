package edu.iu.jrsalata;

/**
 * The NumSystem enum represents different numerical systems.
 * It provides three constants to specify the numerical system:
 * <ul>
 *   <li>{@link #HEX} - Hexadecimal system</li>
 *   <li>{@link #DEC} - Decimal system</li>
 *   <li>{@link #BIN} - Binary system</li>
 * </ul>
 */
public enum NumSystem {
    HEX, DEC, BIN
}
